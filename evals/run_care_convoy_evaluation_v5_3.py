import os
import sys
import mlflow
from mlflow.genai.datasets import get_dataset
from mlflow.genai.scorers import list_scorers

PROJECT_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
if PROJECT_ROOT not in sys.path:
    sys.path.insert(0, PROJECT_ROOT)

# Use existing environment variables if set, otherwise use defaults from generation time
os.environ.setdefault("MLFLOW_TRACKING_URI", "databricks")
os.environ.setdefault("MLFLOW_EXPERIMENT_ID", "1541130027862350")

print("=" * 60)
print("MLflow Agent Evaluation")
print("=" * 60)
print()

# Configuration
DATASET_NAME = "workspace.default.care_convoy_eval_v5_3"
EXPERIMENT_ID = "1541130027862350"

# Load dataset
print("Loading evaluation dataset...")
try:
    dataset = get_dataset(DATASET_NAME)
    df = dataset.to_df()
    print(f"  Dataset: {DATASET_NAME}")
    print(f"  Records: {len(df)}")

    # Show sample input structure
    if len(df) > 0:
        sample_inputs = df.iloc[0]['inputs']
        print(f"  Input keys: {list(sample_inputs.keys())}")
    print()
except Exception as e:
    print(f"✗ Failed to load dataset: {e}")
    sys.exit(1)

# Get registered scorers from the experiment
print("Loading registered scorers...")
try:
    registered_scorers = list_scorers(experiment_id=EXPERIMENT_ID)

    if not registered_scorers:
        print("  ⚠ No registered scorers found in experiment")
        print("  Register scorers first with: scorer.register()")
        sys.exit(1)

    print(f"  Found {len(registered_scorers)} registered scorer(s):")
    for scorer in registered_scorers:
        print(f"    - {scorer.name}")
    print()
except Exception as e:
    print(f"✗ Failed to load scorers: {e}")
    sys.exit(1)

# Import the agent entry point
# IMPORTANT: The function signature must match the dataset's input keys
print("Importing agent entry point...")
try:
    from evals.care_convoy_eval_adapter import evaluate_agent
    print(f"  ✓ Imported evaluate_agent from evals.care_convoy_eval_adapter")
    print()
except ImportError as e:
    print(f"✗ Failed to import agent: {e}")
    sys.exit(1)

# Run evaluation
print("=" * 60)
print("Running Evaluation")
print("=" * 60)
print()
print(f"  Dataset: {DATASET_NAME} ({len(df)} records)")
print(f"  Scorers: {[s.name for s in registered_scorers]}")
print(f"  Entry point: evals.care_convoy_eval_adapter.evaluate_agent")
print()

try:
    results = mlflow.genai.evaluate(
        data=df,
        predict_fn=evaluate_agent,
        scorers=registered_scorers,
    )

    print()
    print("=" * 60)
    print("Evaluation Results")
    print("=" * 60)
    print()

    # Display aggregate metrics
    if hasattr(results, 'metrics') and results.metrics:
        print("Aggregate Metrics:")
        for metric_name, value in results.metrics.items():
            if isinstance(value, float):
                print(f"  {metric_name}: {value:.3f}")
            else:
                print(f"  {metric_name}: {value}")
        print()

    # Save detailed results
    result_table = getattr(results, "result_df", None)
    if result_table is None and hasattr(results, "tables"):
        result_table = results.tables.get("eval_results")
    if result_table is not None:
        output_file = "evals/evaluation_results_full_v5_3.csv"
        result_table.to_csv(output_file, index=False)
        print(f"✓ Detailed results saved to: {output_file}")
        for scorer in registered_scorers:
            column = f"{scorer.name}/value"
            if column in result_table:
                counts = result_table[column].value_counts(dropna=False).to_dict()
                print(f"  {column}: {counts}")
    if hasattr(results, "run_id"):
        print(f"  MLflow run ID: {results.run_id}")

    print()
    print("=" * 60)
    print("Evaluation Complete")
    print("=" * 60)

except Exception as e:
    print(f"✗ Evaluation failed: {e}")
    print()
    print("Common issues:")
    print("  1. Function signature mismatch - entry point params must match dataset input keys")
    print(f"     Dataset input keys: {list(sample_inputs.keys()) if 'sample_inputs' in dir() else 'unknown'}")
    print("     Entry point must accept these as keyword arguments")
    print("  2. Missing dependencies - check agent imports")
    print("  3. Authentication issues - verify OPENAI_API_KEY or other credentials")
    import traceback
    traceback.print_exc()
    sys.exit(1)
